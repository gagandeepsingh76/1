class variable {
    public static void main(String args[]) {

        // byte: 8-bit integer
        byte age = 18;
        System.out.println("byte value: " + age);

        // short: 16-bit integer
        short var2 = 5666;
        System.out.println("short value: " + var2);

        // int: 32-bit integer
        int var1 = 256;
        System.out.println("int value: " + var1);

        // long: 64-bit integer (suffix L is required for long literals)
        long longVar = 900L;
        System.out.println("long value: " + longVar);

        // float: single-precision 32-bit floating point (suffix f is required)
        float price = 100.05f;
        System.out.println("float value: " + price);

        // double: double-precision 64-bit floating point
        double doubleVar = 12345.6789;
        System.out.println("double value: " + doubleVar);

        // boolean: true or false
        boolean isJavaFun = true;
        System.out.println("boolean value: " + isJavaFun);

        // char: single 16-bit Unicode character
        char letter = 'A';
        System.out.println("char value: " + letter);
    }
}
