public class constant {
    public static void main(String[] args) {
        final double PI = 3.14159;
        double radius = 5.0;
        double area = PI * radius * radius;

        System.out.println("Area of circle: " + area);
    }
}
// This code defines a constant for the value of PI and uses it to calculate the area of a circle with a given radius.
// The constant PI is declared as final, meaning its value cannot be changed after initialization.
